    private void create_tabpi(long total_piutang, long sisa_piutang) {
        long tot_byr=0;
        int i=1;
        SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat dt2 = new SimpleDateFormat("dd/MM/yyyy");
        try{
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery("select*from pembayaran_piutang where no_piutang ='"+txt_nopiutang2.getText().trim()+"'");
                while(rs.next()){
                    Date tgl = dt.parse(rs.getString("tgl_byr"));
                    String tanggal = dt2.format(tgl)+" "+rs.getString("jam_byr");
                    tot_byr+=Long.parseLong(rs.getString("jum_byr"));
                    mdl3.addRow(new Object[]{i,rs.getString("no_piutang"),tanggal,set_Rp(Long.parseLong(rs.getString("Jum_byr"))),set_Rp(Long.parseLong(rs.getString("sisa_piutang"))),rs.getString("keterangan")});
                    i++;
                }
                jTable2.setModel(mdl3);
                lbl_totbyr.setText(set_Rp(tot_byr));
                lbl_sisapiutang.setText(set_Rp(sisa_piutang));
                lbl_total.setText(set_Rp(total_piutang));
        }catch (Exception e){
            e.printStackTrace();
        }
    }

