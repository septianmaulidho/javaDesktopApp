    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        pencairan=0;
        SimpleDateFormat dt=new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat dt2=new SimpleDateFormat("dd/MM/yyyy");
        tampil_depo=jComboBox1.getSelectedItem().toString();
        if(tampil_depo.equals("Tampilkan Riwayat Deposit")){
            bersihkan_Table();
            try{
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery("select*from deposit where no_deposit ='"+txt_nodeptable.getText().trim()+"'");
                rs.next();
                int a = rs.getRow();
                if(a>0){
                    tot_deposit=0;
                    Date tgl = dt.parse(rs.getString("Tgl"));
                    String tanggal = dt2.format(tgl)+" "+rs.getString("jam");
                    mdl.addRow(new Object [] {1,rs.getString("no_deposit"),rs.getString("nama"),tanggal,rs.getString("Aktivitas"),set_Rp(Long.parseLong(rs.getString("jum_deposit"))),rs.getString("keterangan")});
                    tot_deposit+=Long.parseLong(rs.getString("jum_deposit"));
                    jTable1.setModel(mdl);
                    cari_tmb();
                    long sisa_saldo = Long.parseLong(rs.getString("Saldo"));
                    lbl1.setText("Total Deposit");
                    value1.setText(set_Rp(tot_deposit));
                    lbl2.setText("Sisa Saldo");
                    value2.setText(set_Rp(sisa_saldo));
                    lbl3.setText("Total Transaksi");
                    value3.setText(set_Rp(tot_deposit - sisa_saldo-pencairan));
                }
                else
                JOptionPane.showMessageDialog(this,"Tidak ada data ditemukan!","Informasi",JOptionPane.INFORMATION_MESSAGE);
                rs.close();
                smt.close();
                cn.close();
            }catch (Exception e){
                e.printStackTrace();
            }
        }
        else{
            bersihkan_Table2();
            try{
                int i=1; long tot_trans=0;
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery("select*from nota where no_deposit ='"+txt_nodeptable.getText().trim()+"'");
                while(rs.next()){
                    Date tgl = dt.parse(rs.getString("Tgl_trans"));
                    String tanggal = dt2.format(tgl)+" "+rs.getString("Wkt_trans");
                    mdl2.addRow(new Object [] {i,rs.getString("No_trans"),tanggal,set_Rp(Long.parseLong(rs.getString("Sub_total"))),set_Rp(Long.parseLong(rs.getString("Diskon"))),set_Rp(Long.parseLong(rs.getString("Total"))),rs.getString("Jenis_pembayaran"),set_Rp(Long.parseLong(rs.getString("deposit")))});
                    tot_trans+=Long.parseLong(rs.getString("deposit"));
                    i++;
                }
                jTable1.setModel(mdl2);
                lbl1.setText("Total Transaksi");
                value1.setText(set_Rp(tot_trans));
                lbl2.setText("Sisa Saldo");
                long sisal=getsisal();
                value2.setText(set_Rp(sisal));
                lbl3.setText("Total Deposit");
                value3.setText(set_Rp(tot_trans+sisal));
            }catch (Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

