    public String set_Rp(Long bil){
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');

        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        return kursIndonesia.format(bil);
    }
    
    public void Uplabel(){
        Date ys = new java.util.Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
        long tot_pen=0,tot_byr=0;
        try{
            Class.forName(JDBC_DRIVER);
            Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
            Statement smt = cn.createStatement();
            ResultSet rs = smt.executeQuery("select* from nota where Tgl_trans = '"+ String.valueOf(s.format(ys))+"'");
            while(rs.next())
               tot_pen+=Long.parseLong(rs.getString("Total"));
            lbl_penjualan.setText(String.valueOf(set_Rp(tot_pen)));
            rs.last();
            lbl_trans.setText(String.valueOf(rs.getRow()));
            lbl_stk.setText(String.valueOf(get_stokmin()));
            lbl_piutangjt.setText(String.valueOf(get_piutangjt()));
            tot_byr=get_pembayaran(String.valueOf(s.format(ys)));
            lbl_pembayaran.setText(set_Rp(tot_byr));
            lbl_omset.setText(set_Rp(tot_pen-tot_byr));
            rs.close();
            smt.close();
            cn.close(); 
        }catch (Exception e){
            e.printStackTrace();
        }
    }
    public long get_pembayaran(String tgl){
        long tot_byr=0;
        try{
            Class.forName(JDBC_DRIVER);
            Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
            Statement smt = cn.createStatement();
            ResultSet rs3 = smt.executeQuery("select* from pengeluaran where Tgl_slip = '"+tgl+"'");
            while(rs3.next()){
                tot_byr+=Long.parseLong(rs3.getString("Total_bayar"));
            }
        }catch (Exception e){
            e.printStackTrace();
        }
        return tot_byr;
    }
