    private void generate_table(String sql) {
        int i=1;
        long sisa_byr=0;
        bersihkan_Table();
        
        try{
            Class.forName(JDBC_DRIVER);
            Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
            Statement smt = cn.createStatement();
            ResultSet rs = smt.executeQuery(sql);
            while(rs.next()){
                Date tgl = df.parse(rs.getString("Tanggal"));
                Date tgl_jt = df.parse(rs.getString("Tanggal_jt"));
                String tanggal = df2.format(tgl);
                String tanggal_jt = df2.format(tgl_jt);
                sisa_byr+=Long.parseLong(rs.getString("Sisa_pembayaran"));
                mdl.addRow(new Object[]{i,rs.getString("no_nota"),rs.getString("nama_distributor"),rs.getString("nama_sales"),tanggal,tanggal_jt,set_Rp(Long.parseLong(rs.getString("Total_pembayaran"))),set_Rp(Long.parseLong(rs.getString("Sisa_pembayaran"))),rs.getString("pembayaran"),rs.getString("keterangan")});
                i++;
            }
            lbl_sisabyr.setText(set_Rp(sisa_byr));
        }catch (Exception e){
                e.printStackTrace();
        }
    }
    
    public String set_Rp(Long bil){
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');

        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        return kursIndonesia.format(bil);
    }
    
