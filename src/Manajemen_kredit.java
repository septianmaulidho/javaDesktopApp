    private void generate_tabhis(String no_nota) {
        int i=1;
        long sdh_byr=0;
        bersihkan_Table2();
        try{
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery("select*from pengeluaran where no_kwitansi='"+no_nota+"' AND jenis_pembayaran='Pembayaran Kredit'");
                while(rs.next()){
                    Date tgl = df.parse(rs.getString("tgl_slip"));
                    mdl2.addRow(new Object[]{i,rs.getString("no_Slip"),df2.format(tgl),rs.getString("bayar_kepada"),rs.getString("penerima"),set_Rp(Long.parseLong(rs.getString("Total_bayar"))),rs.getString("Pembayaran_untuk")});
                    sdh_byr+=Long.parseLong(rs.getString("Total_bayar"));
                    i++;
                }
                rs.last();
                if(rs.getRow()<=0)
                    JOptionPane.showMessageDialog(this,"Belum ada data pembayaran!","Informasi",JOptionPane.INFORMATION_MESSAGE);
                long tot_byr=get_totalbayar(no_nota);
                lbl_totbyr.setText(set_Rp(tot_byr));
                lbl_sdhbyr.setText(set_Rp(sdh_byr));
                lbl_sisabyr2.setText(set_Rp(tot_byr-sdh_byr));
                rs.close();
                smt.close();
            }catch (Exception e){
                e.printStackTrace();
            }
    }

