    public void simpan_data(){
        String slip_no = Ge_slipno();
        Date ys = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat s2 = new SimpleDateFormat("kk:mm:ss");
        SimpleDateFormat s3 = new SimpleDateFormat("dd/MM/yyyy kk:mm:ss");
        String tgl = s.format(ys);
        String wkt = s2.format(ys);
        String time = s3.format(ys);
        String pil=jComboBox2.getSelectedItem().toString();
        try{
            Class.forName(JDBC_DRIVER);
            Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
            Statement smt = cn.createStatement();
            int a = smt.executeUpdate("INSERT INTO Pengeluaran (No_slip,jenis_pembayaran,Tgl_slip,Jam_slip,Bayar_kepada,Penerima,Total_bayar,Terbilang,No_kwitansi,Pembayaran_untuk) values"
                 +"('"+slip_no+"','"+pil+"','"+tgl+"','"+wkt+"','"+txtbyr_kpd.getText().trim()+"','"+txt_penerima.getText().trim()
                 +"','"+txt_jumbyr.getText().trim()+"','"+txt_terbilang.getText().trim()+"','"+txtno_kwitansi.getText().trim()
                 +"','"+txtbyr_unt.getText().trim()+"') ");
            if(a>0){
               String pesan = "Data berhasil disimpan, apakah anda akan mencetak bukti pembayaran?";
               int result = JOptionPane.showConfirmDialog(null,pesan,"Confirmation Message",JOptionPane.YES_NO_OPTION);
               if(result == JOptionPane.YES_OPTION){
                        HashMap parameter = new HashMap();
                        JasperDesign jdesign = JRXmlLoader.load("src/Slip.jrxml");
                        parameter.put("No_slip",slip_no);
                        parameter.put("Time",time);
                        parameter.put("untuk",txtbyr_kpd.getText().trim());
                        parameter.put("penerima",txt_penerima.getText().trim());
                        parameter.put("jumlah",set_Rp(Long.parseLong(txt_jumbyr.getText().trim())));
                        parameter.put("terbilang",txt_terbilang.getText().trim());
                        parameter.put("untuk_bayar",txtbyr_unt.getText().trim());
                        String query = "select * from pengeluaran where No_Slip = '"+slip_no+"'";
                        JRDesignQuery updateQuery = new JRDesignQuery();
                        updateQuery.setText(query);
                        jdesign.setQuery(updateQuery);
                        JasperReport jreport = JasperCompileManager.compileReport(jdesign);
                        JasperPrint jprint = JasperFillManager.fillReport(jreport,parameter,cn);
                        JasperViewer.viewReport(jprint,false);
                    }
                }
                smt.close();
                cn.close();
                bersihkan_inputan();
            }catch (Exception e){
                e.printStackTrace();
                }
    }

