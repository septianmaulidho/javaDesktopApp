public String Ge_slipno(){
        String no_slip = "SL";
        
        Date ys = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyyMMdd");
        no_slip = no_slip.concat(s.format(ys)).concat(no_urut(s.format(ys)));
        return no_slip;
    }
    
    public String no_urut(String tgl){
        int no;
        try{
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery("select * from pengeluaran where No_Slip like '%" + tgl +"%' order by No_Slip asc");
                rs.last();
                if(rs.getRow()==0)
                    no = 1;
                else
                    no=Integer.parseInt(rs.getString("No_Slip").substring(10,13))+1;
                
                if(no<10)
                    return "00".concat(String.valueOf(no));
                else if(no>=10&&no<100)
                    return "0".concat(String.valueOf(no));
                else
                    return String.valueOf(no);
            } catch (Exception e){
                e.printStackTrace();
                return null;
            } 
    }
    
    public String set_Rp(Long bil){
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();

        formatRp.setCurrencySymbol("Rp ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');

        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        return kursIndonesia.format(bil);
    }
