    public void create_table(String sql){
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat df2 = new SimpleDateFormat("yyyy-MM-dd");
        int i=1;
        tot_byr = 0;
        try{
                Class.forName(JDBC_DRIVER);
                Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                Statement smt = cn.createStatement();
                ResultSet rs = smt.executeQuery(sql);
                while(rs.next()){
                    Date d1 = df2.parse(rs.getString("Tgl_slip"));
                    mdl.addRow(new Object [] {i,rs.getString("No_Slip"),rs.getString("jenis_pembayaran"),df.format(d1).concat(" ").concat(rs.getString("Jam_slip")),
                    rs.getString("Bayar_kepada"),rs.getString("Penerima"),rs.getString("No_kwitansi"),rs.getString("Pembayaran_untuk")
                    ,rs.getString("Terbilang"),set_Rp(Long.parseLong(rs.getString("Total_bayar")))});
                    tot_byr += Long.parseLong(rs.getString("Total_bayar"));
                    i++;
                }
                rs.last();
                if(rs.getRow()==0)
                    JOptionPane.showMessageDialog(this,"Tidak ada data ditemukan!","Informasi",JOptionPane.INFORMATION_MESSAGE);
                lbl_totalbyr.setText(set_Rp(tot_byr));
        } catch (Exception e){
                e.printStackTrace();
            } 
    }
    
