    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        bersihkan_Table();
        SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat dt2 = new SimpleDateFormat("dd/MM/yyyy");
        String pil = jComboBox1.getSelectedItem().toString();
        String sql;
        switch (pil) {
            case "Semua Pembayaran":
                sql = "select * from pengeluaran where Tgl_slip between '"+String.valueOf(dt.format(tgl_awl.getDate()))+"' AND '"+String.valueOf(dt.format(tgl_akhir.getDate()))+"' order by No_slip asc";
                pencarian = "data pembayaran "+String.valueOf(dt2.format(tgl_awl.getDate()))+" sampai "+String.valueOf(dt2.format(tgl_akhir.getDate()));
                break;
            case "Tujuan Pembayaran":
                sql = "select * from pengeluaran where Bayar_kepada like '%"+txt_key.getText().trim()+"%' order by No_slip asc";
                pencarian = "data pembayaran dengan Tujuan Pembayaran: "+txt_key.getText().trim();
                break;
            case "Nomor Slip":
                sql = "select * from pengeluaran where No_Slip = '"+txt_key.getText().trim()+"'";
                pencarian= "data pembayaran dengan Nomor Slip: "+txt_key.getText().trim();
                break;
            default:
                sql = "select * from pengeluaran where No_kwitansi = '"+txt_key.getText().trim()+"' AND Tgl_slip between '"+String.valueOf(dt.format(tgl_awl.getDate()))+"' AND '"+String.valueOf(dt.format(tgl_akhir.getDate()))+"' order by No_slip asc";
                pencarian = "data pembayaran No. Kwitansi: "+txt_key.getText().trim()+" dan "+String.valueOf(dt2.format(tgl_awl.getDate()))+" sampai "+String.valueOf(dt2.format(tgl_akhir.getDate()));
                break;
        }
        create_table(sql);
    }//GEN-LAST:event_jButton1ActionPerformed

