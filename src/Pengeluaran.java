    private void Hapus_dataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Hapus_dataActionPerformed
        // TODO add your handling code here:
        int j_brs = jTable1.getRowCount()-1;
        Date ys = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat s2 = new SimpleDateFormat("kk:mm:ss");
        String tgl = s.format(ys);
        String wkt = s2.format(ys);
        String pem_kre="";
        if(j_brs>=0){
            String pesan = "Anda yakin menghapus "+pencarian+"?";
            int result = JOptionPane.showConfirmDialog(null,pesan,"Confirmation Message",JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                try{
                    Class.forName(JDBC_DRIVER);
                    Connection cn = DriverManager.getConnection(DB_URL,USER,PASS);
                    Statement smt = cn.createStatement();
                    int i = j_brs;
                    while(i>=0){
                        if(!jTable1.getModel().getValueAt(i,2).toString().equals("Pembayaran Kredit")){
                            smt.executeUpdate("delete from pengeluaran where No_slip = '"+jTable1.getModel().getValueAt(i,1)+"' "); 
                            mdl.removeRow(i);
                        }
                        else
                            pem_kre="Ada";
                        i=i-1;
                    }
                    if(pem_kre.equals("Ada"))
                        JOptionPane.showMessageDialog(this,"Tidak dapat menghapus data Pembayaran Kredit","Informasi",JOptionPane.INFORMATION_MESSAGE);
                    smt.executeUpdate("insert into log_hapus (Tgl_hapus,Jam_hapus,Data_dihapus) values"
                              +"('"+tgl+"','"+wkt+"','"+pencarian+"')");
                } catch (Exception e){
                    e.printStackTrace();
                    }
            }
        }
        else
            JOptionPane.showMessageDialog(this,"Tidak ada data terhapus!","Informasi",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_Hapus_dataActionPerformed

